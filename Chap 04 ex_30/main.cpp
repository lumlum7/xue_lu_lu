#include <iostream>
using namespace std;
int main()
{
    double r,diameter,perimeter,area;
    cout<<"Enter a number:";
    cin>>r;
    diameter=2*r;
    perimeter=2*3.14159*r;
    area=3.14159*r*r;
    cout<<"The diameter is  "<<diameter<<endl;
    cout<<"The perimeter is  "<<perimeter<<endl;
    cout<<"The area is  "<<area<<endl;
}
